const User = require("../models/userModel");
const Authentication = require("../../middleware/jwtAuthentication");
const model = new User();
const authentication = new Authentication();

exports.verifyToken = function(req, res) {
    authentication.authentication(req, req.body.role, model.verifyToken, null, function (status, data, message, error) {
        res.send({
            status: status,
            data: data,
            message: message,
            error: error
        })
    }).then(value => {
        console.log(value);
    });
};

